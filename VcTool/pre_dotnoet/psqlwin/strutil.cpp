#include "strutil.h"



//--------------------------------------------------------------------------------------------------------------------
//	関数名：int htoi( char *hexstr , short len )
//	機能　：16進（文字列）を10進(short型)に変換する
//	引数　：変換元データ格納ポインタ
//	返値　：変換後データ
//--------------------------------------------------------------------------------------------------------------------
int	htoi( char *hexstr , int len )
{
	int ret = 0;
	int i;

	if( len > 4 ) return 0;
	for( i = 0 ; i < len ; i++ ){
		ret *= 16;
		if( hexstr[i] < 'A' ) ret += hexstr[i] - '0';
		else
		if( hexstr[i] < 'a' ) ret += hexstr[i] - 'A'+10;
		else
			ret += hexstr[i] - 'a'+10;
	}
	return(ret);
}
